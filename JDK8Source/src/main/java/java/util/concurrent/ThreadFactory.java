package java.util.concurrent;

/**
 * An object that creates new threads on demand.  Using thread factories
 * removes hardwiring of calls to {@link Thread#Thread(Runnable) new Thread},
 * enabling applications to use special thread subclasses, priorities, etc.
 *
 * <p>
 * The simplest implementation of this interface is just:
 *  <pre> {@code
 * class SimpleThreadFactory implements ThreadFactory {
 *   public Thread newThread(Runnable r) {
 *     return new Thread(r);
 *   }
 * }}</pre>
 *
 * The {@link Executors#defaultThreadFactory} method provides a more
 * useful simple implementation, that sets the created thread context
 * to known values before returning it.
 * <p>
 *  根据需要创建新线程的对象。使用线程工厂删除对{@link Thread#Thread(Runnable)new Thread}的调用的硬线连接,使应用程序能够使用特殊的线程子类,优先级等。
 * 
 * <p>
 *  这个接口的最简单的实现是：<pre> {@code类SimpleThreadFactory实现ThreadFactory {public Thread newThread(Runnable r){return new Thread(r); }
 * }} </pre>。
 * 
 * 
 * @since 1.5
 * @author Doug Lea
 */
public interface ThreadFactory {

    /**
     * Constructs a new {@code Thread}.  Implementations may also initialize
     * priority, name, daemon status, {@code ThreadGroup}, etc.
     *
     * <p>
     *  {@link Executors#defaultThreadFactory}方法提供了一个更有用的简单实现,它将返回之前创建的线程上下文设置为已知的值。
     * 
     * 
     * @param r a runnable to be executed by new thread instance
     * @return constructed thread, or {@code null} if the request to
     *         create a thread is rejected
     */
    Thread newThread(Runnable r);
}
